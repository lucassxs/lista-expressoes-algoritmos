# Faça um algoritmo que receba três números, calcule e mostre a média aritmética entre eles

a = int(input('Digite a variável a: '))
b = int(input('Digite a variável b: '))
c = int(input('Digite a variável c: '))
resultado = ((a + b + c)/3)
print('O resultado da média entre {}, {} e {} é {}!'.format(a, b, c, resultado))